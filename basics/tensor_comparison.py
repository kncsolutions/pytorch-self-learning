import torch

BREAK_SIZE = 70


def comparison_operations():

    print("=" * BREAK_SIZE)
    print("PYTORCH COMPARISON OPERATIONS PLAYGROUND")
    print("=" * BREAK_SIZE)

    print(f"PyTorch Version : {torch.__version__}")

    print("=" * BREAK_SIZE)

    ####################################################################
    # BASE TENSORS
    ####################################################################

    x = torch.tensor([
        [1, 5, 3],
        [7, 2, 9]
    ])

    y = torch.tensor([
        [1, 4, 8],
        [6, 2, 10]
    ])

    print("Tensor x :")
    print(x)

    print("\nTensor y :")
    print(y)

    ####################################################################
    # torch.eq()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.eq()")

    ####################################################################
    # DESCRIPTION:
    #
    # eq = equal
    #
    # Compares corresponding elements.
    #
    # Returns:
    # True  -> if equal
    # False -> otherwise
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij == y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # for every element:
    #
    # if x_ij == y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z1 = torch.eq(x, y)

    print(z1)

    ####################################################################
    # torch.ne()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.ne()")

    ####################################################################
    # DESCRIPTION:
    #
    # ne = not equal
    #
    # Returns True where elements differ.
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij != y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if x_ij != y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z2 = torch.ne(x, y)

    print(z2)

    ####################################################################
    # torch.gt()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.gt()")

    ####################################################################
    # DESCRIPTION:
    #
    # gt = greater than
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij > y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if x_ij > y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z3 = torch.gt(x, y)

    print(z3)

    ####################################################################
    # torch.lt()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.lt()")

    ####################################################################
    # DESCRIPTION:
    #
    # lt = less than
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij < y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if x_ij < y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z4 = torch.lt(x, y)

    print(z4)

    ####################################################################
    # torch.ge()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.ge()")

    ####################################################################
    # DESCRIPTION:
    #
    # ge = greater than or equal
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij >= y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if x_ij >= y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z5 = torch.ge(x, y)

    print(z5)

    ####################################################################
    # torch.le()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.le()")

    ####################################################################
    # DESCRIPTION:
    #
    # le = less than or equal
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij = (x_ij <= y_ij)
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if x_ij <= y_ij:
    #     True
    # else:
    #     False
    #
    ####################################################################

    z6 = torch.le(x, y)

    print(z6)

    ####################################################################
    # torch.where()
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("method torch.where()")

    ####################################################################
    # DESCRIPTION:
    #
    # Conditional selection operation.
    #
    # Selects values depending on condition.
    #
    ####################################################################

    ####################################################################
    # MATHEMATICAL FORMULATION:
    #
    # z_ij =
    #
    #     a_ij   if condition is True
    #
    #     b_ij   otherwise
    #
    ####################################################################

    ####################################################################
    # BASIC ALGORITHM:
    #
    # if condition:
    #     select from tensor A
    # else:
    #     select from tensor B
    #
    ####################################################################

    condition = x > y

    print("Condition (x > y) :")
    print(condition)

    z7 = torch.where(condition, x, y)

    print("\nSelected tensor :")
    print(z7)

    ####################################################################
    # PRACTICAL EXAMPLE
    ####################################################################

    print("\n" + "#" * BREAK_SIZE)
    print("PRACTICAL EXAMPLE")

    ####################################################################
    # ReLU-like behavior using torch.where()
    ####################################################################

    sample = torch.tensor([
        [-3.0, -1.0, 2.0],
        [4.0, -5.0, 6.0]
    ])

    print("Original Tensor :")
    print(sample)

    relu_like = torch.where(
        sample > 0,
        sample,
        torch.tensor(0.0)
    )

    print("\nReLU-like Output :")
    print(relu_like)

    ####################################################################
    # Important Insight:
    #
    # torch.where() is extremely important in:
    #
    # - masking
    # - transformers
    # - attention systems
    # - filtering
    # - conditional tensor operations
    # - reinforcement learning
    #
    ####################################################################

    ####################################################################
    # FINAL SUMMARY
    ####################################################################

    print("\n" + "=" * BREAK_SIZE)
    print("COMPARISON OPERATIONS SUMMARY")
    print("=" * BREAK_SIZE)

    print("""
Comparison operations create BOOLEAN tensors.

True  -> condition satisfied
False -> condition failed

These operations are foundational for:

- masking
- filtering
- conditional selection
- attention mechanisms
- thresholding
- classification systems
- logical tensor operations
""")

    print("=" * BREAK_SIZE)


if __name__ == "__main__":

    comparison_operations()